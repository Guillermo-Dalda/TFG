function val = pressure_vessel_design(x)
   val = 0.6224 * x(1) * x(3) * x(4) + 1.7781 * x(2) * x(3)*x(3) + 3.1661 * x(1)*x(1) * x(4) + 19.84 * x(1)*x(1) * x(3);
   g1 = -x(1) + 0.0193 * x(3);
   g2 = -x(2) + 0.00954 * x(3);
   g3 = -pi * x(3)*x(3) * x(4) - (4.0 / 3.0) * pi * x(3)*x(3)*x(3) + 1296000;
   g4 = x(4) - 240;

   if (g1 > 0 || g2 > 0 || g3 > 0 || g4 > 0)
        val = val + 10000 * (g1>0) + 10000 * (g2>0) + 10000 * (g3>0) + 10000 * (g4>0) + g1 * (g1>0) + g2 * (g2>0) + g3 * (g3>0) + g4 * (g4>0);
   end
end
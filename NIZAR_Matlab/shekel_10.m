function val = shekel_10(x)
   val = 0;
   a = [4.0 4.0 4.0 4.0
        1.0 1.0 1.0 1.0
        8.0 8.0 8.0 8.0
        6.0 6.0 6.0 6.0
        3.0 7.0 3.0 7.0
        2.0 9.0 2.0 9.0
        5.0 5.0 3.0 3.0
        8.0 1.0 8.0 1.0
        6.0 2.0 6.0 2.0
        7.0 3.6 7.0 3.6];
   c = [0.1 0.2 0.2 0.4 0.4 0.6 0.3 0.7 0.5 0.5];
   for i=1:10
       sum = 0;
       for j=1:4
           sum = sum + (x(j) - a(i,j)) * (x(j) - a(i,j));
       end
       val = val + 1 / (sum + c(i));
   end
   val = -1 * val;
end